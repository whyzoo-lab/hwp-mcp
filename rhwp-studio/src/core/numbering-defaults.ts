export interface OutlinePreset {
  /** 프리셋 이름 (KO) */
  name: string;
  /** 프리셋 이름 (EN) */
  nameEn: string;
  /** 레벨별 서식 문자열 (^1, ^2 등은 해당 레벨 번호로 치환) */
  format: string[];
  /** 레벨별 속성 원본 문자열 (size;...;numberType;...;) */
  attrs: string[];
}

/**
 * 개요 서식 문자열을 레벨별 배열로 파싱
 * 구분자: [::]
 */
function parseFormat(raw: string): string[] {
  return raw.split('[::]').filter((_, i, arr) => i < arr.length - 1);
}

function parseAttrs(raw: string): string[] {
  return raw.split('[::]').filter((_, i, arr) => i < arr.length - 1);
}

